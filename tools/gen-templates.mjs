#!/usr/bin/env node
// Generates IDE-native starter templates from templates/catalogue.json.
// See templates/README.md. Run: node tools/gen-templates.mjs [--check]

import { readFileSync, writeFileSync, mkdirSync, readdirSync, rmSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
// Emitted as TypeScript, not JSON: `src/` is excluded from the VSIX, so the
// catalogue has to be bundled into out/main.js rather than read at runtime.
const VSCODE_OUT = join(ROOT, 'bbj-vscode', 'src', 'templates', 'generated-templates.ts')
const INTELLIJ_OUT = join(ROOT, 'bbj-intellij', 'src', 'main', 'resources', 'fileTemplates', 'internal')

/** Matches a declared placeholder, e.g. ${ProgramName}. */
function placeholderRe(varNames) {
    return new RegExp('\\$\\{(' + varNames.map(escapeRe).join('|') + ')\\}', 'g')
}
function escapeRe(s) {
    return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

/**
 * Split a template body into literal and placeholder segments.
 * Anything not matching a *declared* placeholder is literal — including BBj's
 * own `$`, which must never be handed to a templating engine unescaped.
 */
export function segment(body, varNames) {
    const re = placeholderRe(varNames)
    const segments = []
    let last = 0
    let m
    while ((m = re.exec(body)) !== null) {
        if (m.index > last) segments.push({ literal: body.slice(last, m.index) })
        segments.push({ variable: m[1] })
        last = m.index + m[0].length
    }
    if (last < body.length) segments.push({ literal: body.slice(last) })
    return segments
}

/**
 * Render to an IntelliJ File and Code Template (Velocity).
 *
 * BBj string variables end in `$` (`title$`), which Velocity reads as a `$title`
 * reference and silently swallows. Every literal run containing `$` or `#` is
 * therefore wrapped in a Velocity raw block `#[[ ... ]]#`.
 */
export function toVelocity(body, varNames) {
    return segment(body, varNames)
        .map((seg) => {
            if (seg.variable) return '${' + seg.variable + '}'
            if (!/[$#]/.test(seg.literal)) return seg.literal
            if (seg.literal.includes(']]#')) {
                throw new Error('Template literal contains the Velocity raw-block terminator "]]#"; cannot escape safely.')
            }
            return '#[[' + seg.literal + ']]#'
        })
        .join('')
}

function readTemplate(id) {
    const dir = join(ROOT, 'templates', id)
    const meta = JSON.parse(readFileSync(join(dir, 'template.json'), 'utf8'))
    const body = readFileSync(join(dir, meta.source), 'utf8')
    const varNames = meta.variables.map((v) => v.name)

    const declared = new Set(varNames)
    for (const m of body.matchAll(/\$\{([A-Za-z_][A-Za-z0-9_]*)\}/g)) {
        if (!declared.has(m[1])) {
            throw new Error(`${id}: template uses \${${m[1]}} but template.json does not declare it.`)
        }
    }
    return { meta, body, varNames }
}

function renderVscodeModule(templates) {
    return `/**
 * Generated by tools/gen-templates.mjs from templates/ — do not edit.
 *
 * Bodies are verbatim: the extension substitutes \${Var} itself (see
 * new-from-template.ts), so no TextMate snippet parsing is involved and \`$1\`
 * tabstop syntax cannot collide with BBj string variables.
 */

export interface StarterVariable {
    name: string;
    prompt: string;
    default: string;
}

export interface StarterTemplate {
    id: string;
    title: string;
    description: string;
    category: string;
    /** May contain \${Var} placeholders. */
    fileName: string;
    variables: StarterVariable[];
    body: string;
}

export const STARTER_TEMPLATES: StarterTemplate[] = ${JSON.stringify(templates, null, 4)};
`
}

function build() {
    const catalogue = JSON.parse(readFileSync(join(ROOT, 'templates', 'catalogue.json'), 'utf8'))
    const entries = catalogue.templates.map(readTemplate)

    // VS Code: the extension does its own ${Var} substitution, so the body ships
    // verbatim. No TextMate snippet syntax is involved, so `$1`-style tabstop
    // collisions cannot arise.
    const vscode = renderVscodeModule(
        entries.map(({ meta, body }) => ({
            id: meta.id,
            title: meta.title,
            description: meta.description,
            category: meta.category,
            fileName: meta.fileName,
            variables: meta.variables,
            body
        }))
    )

    const intellij = entries.map(({ meta, body, varNames }) => ({
        // IntelliJ derives the visible template name from the file name.
        fileName: meta.title.replace(/\s+/g, '') + '.bbj.ft',
        content: toVelocity(body, varNames)
    }))

    return { vscode, intellij }
}

function write({ vscode, intellij }) {
    mkdirSync(dirname(VSCODE_OUT), { recursive: true })
    writeFileSync(VSCODE_OUT, vscode)

    if (existsSync(INTELLIJ_OUT)) rmSync(INTELLIJ_OUT, { recursive: true })
    mkdirSync(INTELLIJ_OUT, { recursive: true })
    for (const t of intellij) writeFileSync(join(INTELLIJ_OUT, t.fileName), t.content)

    console.log(`wrote ${VSCODE_OUT.replace(ROOT + '/', '')}`)
    for (const t of intellij) console.log(`wrote ${INTELLIJ_OUT.replace(ROOT + '/', '')}/${t.fileName}`)
}

/** CI guard: regeneration must be a no-op against what is committed. */
function check({ vscode, intellij }) {
    const stale = []
    const actualVscode = existsSync(VSCODE_OUT) ? readFileSync(VSCODE_OUT, 'utf8') : null
    if (actualVscode !== vscode) stale.push(VSCODE_OUT)

    const onDisk = existsSync(INTELLIJ_OUT) ? readdirSync(INTELLIJ_OUT).sort() : []
    const expected = intellij.map((t) => t.fileName).sort()
    if (onDisk.join() !== expected.join()) stale.push(INTELLIJ_OUT)
    for (const t of intellij) {
        const p = join(INTELLIJ_OUT, t.fileName)
        if (!existsSync(p) || readFileSync(p, 'utf8') !== t.content) stale.push(p)
    }

    if (stale.length) {
        console.error('Generated templates are stale. Run: node tools/gen-templates.mjs')
        for (const p of stale) console.error('  ' + p.replace(ROOT + '/', ''))
        process.exit(1)
    }
    console.log('generated templates are up to date')
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
    const built = build()
    process.argv.includes('--check') ? check(built) : write(built)
}
